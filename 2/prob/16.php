<?php
    $a = 3;
    $b = 2;

    $a = $a + $b;       // a = 3 + 2 = 5
    $b = $a + 5;        // b = 5 + 5 = 10
    $c = $a * $b;       // c = 5 * 10 = 50

    $c = $c % 2;        // c = 50 % 2 = 0
    $a = $b + $c;       // a = 10 + 0 = 10
    $b = $a * $b;       // b = 10 * 10 = 100

    echo "a: $a, b: $b, c: $c";
?>